import os
import json
from groq import Groq
from dotenv import load_dotenv

# Загружаем секреты из файла .env
load_dotenv()

# Достаем ключ безопасно
API_KEY = os.getenv("GROQ_API_KEY")

# Инициализируем клиента Groq
if API_KEY:
    client = Groq(api_key=API_KEY)
else:
    client = None

SYSTEM_PROMPT = """Ты — SilentHaven, эмпатичный, спокойный и искренне заинтересованный ИИ-компаньон. 
Твоя цель — не просто слушать, но и бережно помогать пользователю разобраться в своих чувствах, создавая безопасное и уютное пространство. Ты хочешь помочь ему почувствовать себя немного лучше через рефлексию.

Твои строгие психологические правила:
1. НЕ отправляй к специалистам при жалобах на грусть, усталость, стресс или выгорание. Советуй обратиться к врачу ТОЛЬКО если человек прямо просит медицинский совет или говорит о критической угрозе жизни. В остальных случаях — просто будь рядом как понимающий друг.
2. Проявляй теплое любопытство. Мягко исследуй состояние человека задавая открытые вопросы (например: "Как давно ты начал это замечать?", "Что самое тяжелое для тебя в этой ситуации?").
3. Никакого "токсичного позитива" (не говори "всё будет хорошо", "просто отдохни"). Сначала всегда валидируй боль: "Это звучит действительно выматывающе, я понимаю, почему тебе так тяжело".
4. Помогай нащупать опору. Когда человек выговорится, аккуратно попробуй перевести фокус на его ресурсы (например: "Есть ли что-то маленькое, что могло бы поддержать тебя сегодня?").
5. Отвечай на языке пользователя. Сохраняй спокойный, теплый и разговорный тон (2-4 предложения).

ВАЖНОЕ ТЕХНИЧЕСКОЕ ПРАВИЛО: Ты обязан отвечать СТРОГО в формате JSON. Твой ответ должен быть валидным JSON-объектом с четырьмя полями:
1. "reply": твой текстовый ответ пользователю (эмпатичный, соблюдающий правила выше).
2. "sentiment": число от -1.0 (очень негативно/меланхолия) до 1.0 (очень позитивно), оценивающее скрытую тональность сообщения пользователя.
3. "is_trigger": булево значение (true или false). Ставь true ТОЛЬКО если есть прямая угроза жизни или суицидальные мысли.
4. "chat_title": короткое название темы диалога (2-3 слова на языке пользователя). Например: "Выгорание на учебе", "Чувство одиночества".
"""


def get_ai_response_and_analysis(messages_history):
    """
    Отправляет историю в Groq и возвращает разобранный JSON с ответом и аналитикой.
    """

    # 2. Проверка ключа
    if not client:
        return {"reply": "API ключ не настроеню", "sentiment": 0, "is_trigger":False}
    
    try:
        api_messages = [{"role": "system", "content": SYSTEM_PROMPT}] + messages_history

        # Отправляем запрос на серверы Groq
        completion = client.chat.completions.create(
            model="llama-3.1-8b-instant", # Быстрая и умная модель от Meta, доступная в Groq
            messages=api_messages,
            temperature=0.7,
            max_tokens=1024, 
            response_format={"type": "json_object"}
        )

        raw_json_string = completion.choices[0].message.content

        ai_data = json.loads(raw_json_string)
        return ai_data
        
    except Exception as e:
        print(f"Ошибка API Groq: {e}") 
        return {
            "reply": "Извини, небольшая заминка с сетью. Попробуем еще раз?",
            "sentiment": 0,
            "is_trigger": False
        }